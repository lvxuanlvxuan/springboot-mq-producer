@RestController
@RequestMapping("controller-direct")
public class DirectModelController {

    @Autowired
    private DirectModelService directModelService;

    @PostMapping("/send")
    public ResponseEntity send(@RequestBody OrderVO vo) {
        for (int i = 1; i <= 10; i++) {
            vo.setNum(i);
            directModelService.send(vo);
        }
        return ResponseEntity.ok().body("success");
    }
}
