@RestController
@RequestMapping("/controller-simple")
public class SimpleModelController {

    @Autowired
    private SimpleModelService simpleModelService;

    @PostMapping("/send")
    public ResponseEntity send(@RequestBody OrderVO orderVO) {
        //调用simple模式发送消息方法
        simpleModelService.send(orderVO);
        return ResponseEntity.ok("success");
    }
}
